#include "libft.h"

char	*ft_strrev(char *str)
{
	int i;
	int b;
	int c;

	i = 0;
	b = 0;
	while (str[i])
		i++;
	i--;
	while (b <= i)
	{
		c = str[b];
		str[b] = str[i];
		str[i] = c;
		b++;
		i--;
	}
	return (str);
}
